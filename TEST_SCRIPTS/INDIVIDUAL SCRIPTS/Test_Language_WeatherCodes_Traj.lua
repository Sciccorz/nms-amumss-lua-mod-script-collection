function getChangeTable(codes)
local ChangeTable = {}
for i=1,#codes do
	local value = codes[i]
	local valueId = value[1]
  
	local stormIntensity = "2"
	if valueId:find("EXTREME", 1, true) ~= nil then
		stormIntensity = "3"
	elseif valueId:find("_CLEAR", 1, true) ~= nil then
		stormIntensity = "1"
	end

	local biomeCode = ""
	if valueId:find("WEATHER_SWAMP", 1, true) == 1 then
		biomeCode = "S"
	elseif valueId:find("WEATHER_LAVA", 1, true) == 1 then
		biomeCode = "V"
	elseif valueId:find("WEATHER_WATERWORLD", 1, true) == 1 then
		biomeCode = "W"
		stormIntensity = "?"
	elseif valueId:find("WEATHER_HEAT", 1, true) == 1 then
		biomeCode = "H"
	elseif valueId:find("WEATHER_COLD", 1, true) == 1 then
		biomeCode = "C"
	elseif valueId:find("WEATHER_TOXIC", 1, true) == 1 then
		biomeCode = "T"
	elseif valueId:find("WEATHER_RADIO", 1, true) == 1 then
		biomeCode = "R"
	elseif valueId:find("WEATHER_LUSH", 1, true) == 1 then
		biomeCode = "U"
	elseif valueId:find("WEATHER_BARREN", 1, true) == 1 then
		biomeCode = "B"
	elseif valueId:find("WEATHER_DEAD", 1, true) == 1 then
		biomeCode = "D"
		stormIntensity = "1"
	elseif valueId:find("WEATHER_CLEAR", 1, true) == 1 then
		biomeCode = "?"
		stormIntensity = "1"
	elseif valueId:find("WEATHER_GLITCH", 1, true) == 1 then
		biomeCode = "G"
		stormIntensity = "1"
	elseif valueId:find("WEATHER_RED", 1, true) == 1 then
		biomeCode = "r"
		stormIntensity = "?"
	elseif valueId:find("WEATHER_GREEN", 1, true) == 1 then
		biomeCode = "g"
		stormIntensity = "?"
	elseif valueId:find("WEATHER_BLUE", 1, true) == 1 then
		biomeCode = "b"
		stormIntensity = "?"
	end

	-- local resultValue = {biomeCode..":"..stormIntensity.." - "} -- original code: a table
  --   >>> a new syntax for newvalue
  
	local resultValue = biomeCode..":"..stormIntensity.." - ".."{:}" -- currently must be a string
	ChangeTable[#ChangeTable+1] = 
						{
							["SPECIAL_KEY_WORDS"] = {"Id", valueId},
							-- ["REPLACE_TYPE"] = "ONCE", -- default value
							["VALUE_CHANGE_TABLE"] 	= 
							{
								{"ENGLISH", resultValue},
							}
						}
end
return ChangeTable
end

